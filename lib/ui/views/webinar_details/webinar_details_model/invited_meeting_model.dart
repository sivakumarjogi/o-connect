    "email": email,
    "contact_id": contactId,
    "name": name,
    "contact_flag": contactFlag,
    "role_id": roleId,
    "profile_pic": profilePic,
    "firstName": firstName,
    "lastName": lastName,
    "is_notified": isNotified,
    "is_invite_accepted": isInviteAccepted,
  };
}
